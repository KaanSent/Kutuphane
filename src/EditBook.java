    public void populateArrayList(){
        try {
            FileInputStream fileGenre = new FileInputStream("Genres.dat");
            ObjectInputStream inputFileGenre = new ObjectInputStream(fileGenre);
            
            boolean endOfFile = false;
            while (!endOfFile){
                try {
                    Genres.add((Genre) inputFileGenre.readObject());
                } catch (EOFException e){
                    endOfFile = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFileGenre.close();            
        } catch (IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        try {
            FileInputStream fileBook = new FileInputStream("Books.dat");
            ObjectInputStream inputFileBook = new ObjectInputStream(fileBook);
            
            boolean endOfFile = false;
            while (!endOfFile){
                try {
                    Books.add((Book) inputFileBook.readObject());
                } catch (EOFException e){
                    endOfFile = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFileBook.close();            
        } catch (IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
     public void saveBooksToFile(){
        try {
            FileOutputStream fileBook = new FileOutputStream("Books.dat");
            ObjectOutputStream outputFileBook = new ObjectOutputStream(fileBook);
            
            for (int i = 0; i<Books.size(); i++){
                outputFileBook.writeObject(Books.get(i));
            }
            outputFileBook.close();
            JOptionPane.showMessageDialog(null, "Action completed successfully!");
            this.dispose();
        } catch (IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

