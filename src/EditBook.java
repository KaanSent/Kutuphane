    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (this.tfBookName.getText().isEmpty() || 
                this.tfAuthorName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all the details");
        } else {
            int selectedIndexBok = cbBook.getSelectedIndex();
            Books.get(selectedIndexBok).setBooksName(tfBookName.getText().trim());
            Books.get(selectedIndexBok).setAuthorsName(tfAuthorName.getText().trim());
            
            
            Genre genre = Genres.get(cbGenre.getSelectedIndex());
            Books.get(selectedIndexBok).setTypeOfBook(genre);
            
            saveBooksToFile();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

